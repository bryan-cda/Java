package br.com.java.marathon.attributions;

public class Attributions {
    public static void main(String[] args) {
        int value = 10;

        System.out.println(value);
        value += 1;
        System.out.println(value);
        value -= 1;
        System.out.println(value);
        value *= 2;
        System.out.println(value);
        value /= 2;
        System.out.println(value);
        value %= 2;
        System.out.println(value);
        value++;
        System.out.println(value);
        value--;
        System.out.println(value);
    }
}
